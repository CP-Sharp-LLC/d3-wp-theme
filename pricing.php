									qry_str += elem[ i ].name + "=" + elem[ i ].value + "&";
								}

								qry_str = qry_str.substring( 0, qry_str.length - 1 );

								$.ajax( {
									type    : "POST",
									url     : "/includes/content-sections/price-calculator-webdesign.php",
									data    : qry_str,
									dataType: "html",
									success : function (msg) {
										document.getElementById( 'show_form' ).innerHTML = msg;
									}
								} );
								return false;
							} );

