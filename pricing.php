							$.each( sliders, function (i, val) {
								$( '#' + i + '-slider' ).slider( {
									slide : function (event, ui) {
										$( '[name=' + i + ']' ).val( sliders[ i ][ 'values' ][ ui.value ] );
									},
									change: function (event, ui) {
										$( '[name=' + i + ']' ).val( sliders[ i ][ 'values' ][ ui.value ] );
